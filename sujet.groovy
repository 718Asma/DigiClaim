Besoin fonctionnel
    Créer une application web qui permet une gestion CRUD des sinistres automobiles. 

        Un sinistre ( en anglais “claim” ) est définie par : 

        N° de sinistre

        Date d’accident

        Date de création

        Statut (Ouvert, Expertise, Terminé)

        Contrat

        N° de contrat

        Date début

        Date fin

        Nom de l’assuré

        N° d'Immatriculation du véhicule

        Photos

    En tant que utilisateur authentifié, je peux : 

        Ajouter/modifier/supprimer un sinistre

        Ajouter/modifier/supprimer une photo du sinistre

        Dans la page détails d’un sinistre, nous devons voir 3 onglets : Sinistre , Contrat et Photos

Exigences techniques 
    Technologies à utiliser  :  

        Angular

        API REST

        Spring 

        PostgeSQL

    Pour la gestion d’authentification vous avez le libre choix par rapport à la technique/technologie

    Livrables
    Conception de la base de données : Tables ou Entities (En mentionnant les relations One-to-One, One-to-Many, etc.)

    Application déployée en local sur votre PC
